/**
 * Generate slug from title
 */
function generateSlug(title) {
  return title
    .toLowerCase()
    .replace(/[^\w\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
}

/**
 * Get admin user ID
 */
async function getAdminUserId() {
  try {
    // Get first admin user
    const { data: roles, error } = await supabase
      .from('user_roles')
      .select('user_id')
      .eq('role', 'admin')
      .limit(1)
      .maybeSingle();

    if (error || !roles) {
      // If no admin found, try to get any user
      const { data: users } = await supabase.auth.admin.listUsers();
      if (users && users.users.length > 0) {
        return users.users[0].id;
      }
      return null;
    }

    return roles.user_id;
  } catch (error) {
    console.error('Error getting admin user:', error);
    return null;
  }
}

/**
 * Get author name from user profile
 */
async function getAuthorName(userId) {
  if (!userId) return 'Admin';

  try {
    const { data: profile } = await supabase
      .from('user_profiles')
      .select('display_name, full_name')
      .eq('user_id', userId)
      .maybeSingle();

    if (profile) {
      return profile.display_name || profile.full_name || 'Admin';
    }

    // Try to get from auth users
    const { data: { users } } = await supabase.auth.admin.listUsers();
    const user = users?.find(u => u.id === userId);
    return user?.email || 'Admin';
  } catch (error) {
    console.error('Error getting author name:', error);
    return 'Admin';
  }
}

